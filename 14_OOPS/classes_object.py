class Student:
    id = 0
    name = ""
    age = 0
    gender = ""


s1 = Student()
s2 = Student()
s1.id = 1
s1.name = "Dr.Doom"
s1.age = 18
s1.gender = "Male"
print(f"s1 = {s1}")
print(f"s1.id = {s1.id}")
print(f"s1.name = {s1.name}")
print(f"s1.age = {s1.age}")
print(f"s1.gender = {s1.gender}")
print(f"s2 = {s2}")
print(f"s2.id = {s2.id}")
print(f"s2.name = {s2.name}")
print(f"s2.age = {s2.age}")
print(f"s2.gender = {s2.gender}")
